/*
If/else : Cung cấp sự linh hoạt hơn với nhiều loại điều kiện, bao gồm so sánh bằng, lớn hơn, nhỏ hơn , ...
switch / case : Thực hiện so sánh giá trị cụ thể với các case
// Sử dụng switch
var day = 3;
switch(day) {
    case 1:
        console.log('Monday');
        break;
    case 2:
        console.log('Tuesday');
        break;
    // ...
    default:
        console.log('Other day');
}

// Sử dụng if-else
var day = 3;
if (day === 1) {
    console.log('Monday');
} else if (day === 2) {
    console.log('Tuesday');
} else {
    console.log('Other day');
}
*/